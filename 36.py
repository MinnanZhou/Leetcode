def func(board):
    ha_r = [{} for i in range(9)]
    ha_c = [{} for i in range(9)]
    ha_b = [[{} for i in range(3)] for i in range(3)]
    for i in range(9):
        for j in range(9):
            if board[i][j] == '.':
                continue
            if board[i][j] in ha_r[i] or board[i][j] in ha_c[j] or board[i][j] in ha_b[i // 3][j // 3]:
                return False
            else:
                ha_r[i][board[i][j]] = [i, j]
                ha_c[j][board[i][j]] = [i, j]
                ha_b[i // 3][j // 3][board[i][j]] = [i, j]
    return True


